    public void LlenarTabla() {
        model.setRowCount(0);
        try {
            String[] Campos = {"Marca", "Modelo", "Talla", "Color"};
            Object[] fila = new Object[4];
            rs = cn.st.executeQuery("SELECT * FROM zapateria");
            model.setColumnIdentifiers(Campos);
            while (rs.next()) {
                fila[0] = rs.getObject(1);
                fila[1] = rs.getObject(2);
                fila[2] = rs.getObject(3);
                fila[3] = rs.getObject(4);
                model.addRow(fila);
            }
            tblZapatos.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(FrmZapateria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

