    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblZapatos.getSelectedRow();
        if (tblZapatos.isRowSelected(fila)) {
            DefaultTableModel dtm = (DefaultTableModel) tblZapatos.getModel();
            try {
                cn.st.executeUpdate("DELETE FROM `zapateria`.`zapateria` WHERE  `Marca`='" + dtm.getValueAt(fila, 0).toString() + "' AND `Modelo`='" + dtm.getValueAt(fila, 1).toString() + "';");
            } catch (SQLException ex) {
                Logger.getLogger(FrmZapateria.class.getName()).log(Level.SEVERE, null, ex);
            }
            LlenarTabla();
        }


    }//GEN-LAST:event_btnEliminarActionPerformed

